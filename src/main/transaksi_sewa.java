    public void tampilan(){
        Object [] baris = {"id_transaksi", "mobil", "harga_sewa", "lama_sewa","total_harga","harga_dp"};
        tabMode = new DefaultTableModel(null, baris);
        tbtransaksi.setModel(tabMode);
        try {
                String sql = "SELECT * FROM tabel_transaksi";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                while (rs.next()){
                        String it = rs.getString("id_transaksi");
                        String mo = rs.getString("mobil");
                        String hs = rs.getString("harga_sewa");
                        String ls = rs.getString("lama_sewa");
                        String th = rs.getString("total_harga");
                        String hd = rs.getString("harga_dp");

                        Object [] data = {it,mo,hs,ls,th,hd};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
    
